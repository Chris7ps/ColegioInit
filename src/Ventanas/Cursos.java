    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (validarDatosRequeridos()) {
            enabled(false);
            Curso cursoActualizar = construirObjetoCurso(true);
            if (Cruds.CrudCurso.insertar(cursoActualizar)) {
                JOptionPane.showMessageDialog(null, "Datos agregados exitosamente");
                limpiar();
                tabla();
            } else {
                JOptionPane.showMessageDialog(null, "Ocurrió un error al agregar curso", "Failure", JOptionPane.ERROR_MESSAGE);
            }
            enabled(true);
            validarAccion();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        enabled(false);
        if (Cruds.CrudCurso.eliminar(Long.valueOf(txtCodigo.getText()))) {
            JOptionPane.showMessageDialog(null, "Curso eliminado exitosamente");
            limpiar();
            tabla();
        } else {
            JOptionPane.showMessageDialog(null, "Ocurrió un error al eliminar curso", "Failure", JOptionPane.ERROR_MESSAGE);
        }
        enabled(true);
        validarAccion();
    }//GEN-LAST:event_btnEliminarActionPerformed

