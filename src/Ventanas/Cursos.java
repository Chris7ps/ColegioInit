    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if (txtBuscarCodigo.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese código", "Failure", JOptionPane.ERROR_MESSAGE);
        } else {
            enabled(false);
            Curso curso = Cruds.CrudCurso.buscarCurso(Long.valueOf(txtBuscarCodigo.getText()));
            if (curso == null) {
                JOptionPane.showMessageDialog(null, "Curso no encontrado", "Failure", JOptionPane.ERROR_MESSAGE);
                txtBuscarCodigo.setText("");
            } else {
                txtCodigo.setText(String.valueOf(curso.getId()));
                txtNombreCurso.setText(curso.getNombreCurso());
                txtDuracion.setText(String.valueOf(curso.getDuracionCurso()));
                cmbProfesor.getModel().setSelectedItem(curso.getProfesor());
            }
            enabled(true);
            validarAccion();
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

