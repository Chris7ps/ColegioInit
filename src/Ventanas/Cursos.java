    private void tabla() {
        try {
            DefaultTableModel tableModel = new DefaultTableModel() {
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
            };
            
            String[] columnas = {"Codigo", "Nombre", "Duracion", "Profesor"};
            tableModel.setColumnIdentifiers(columnas);
            jTable1.setModel(tableModel);
            Object[] fila = new Object[tableModel.getColumnCount()];
            List<Curso> cursos = Cruds.CrudCurso.universo();
            
            for (Curso curso : cursos) {
                fila[0] = curso.getId();
                fila[1] = curso.getNombreCurso();
                fila[2] = curso.getDuracionCurso();
                fila[3] = curso.getProfesor().getNombres() + " " + curso.getProfesor().getApellidos();
                
                tableModel.addRow(fila);
            }
            
        } catch (NullPointerException e) {
        }
    }
    
    private void llenarComboProfesor() {
        DefaultComboBoxModel cbbModel = new DefaultComboBoxModel();
        cmbProfesor.setModel(cbbModel);
        
        List<Profesor> profesores = Cruds.CrudProfesor.universo();
        
        cbbModel.addElement("");
        for (Profesor profesor : profesores) {
            cbbModel.addElement(profesor);
        }
    }
    
    public void limpiar() {
        txtBuscarCodigo.setText("");
        txtCodigo.setText("");
        txtNombreCurso.setText("");
        txtDuracion.setText("");
        cmbProfesor.getModel().setSelectedItem("");
    }
    
    private void validarAccion() {
        if ("".equals(txtCodigo.getText())) {
            btnGuardar.setEnabled(true);
            btnActualizar.setEnabled(false);
            btnEliminar.setEnabled(false);
        } else {
            btnGuardar.setEnabled(false);
            btnActualizar.setEnabled(true);
            btnEliminar.setEnabled(true);
        }
    }
    
    public void enabled(Boolean enabled) {
        btnGuardar.setEnabled(enabled);
        btnActualizar.setEnabled(enabled);
        btnEliminar.setEnabled(enabled);
        btnBuscar.setEnabled(enabled);
    }
    
    private Boolean validarDatosRequeridos() {
        if ("".equals(txtNombreCurso.getText()) || "".equals(txtDuracion.getText())
                || "".equals(cmbProfesor.getSelectedItem())) {
            JOptionPane.showMessageDialog(null, "Llene los campos obligatorios", "Failure", JOptionPane.ERROR_MESSAGE);
            return false;
        } else {
            return true;
        }
    }
    
    private Curso construirObjetoCurso(Boolean crear) {
        Curso curso = new Curso();
        if (!crear) {
            curso.setId(Long.valueOf(txtCodigo.getText()));
        }
        curso.setNombreCurso(txtNombreCurso.getText());
        curso.setDuracionCurso(Integer.valueOf(txtDuracion.getText()));
        curso.setProfesor((Profesor) cmbProfesor.getSelectedItem());
        
        return curso;
    }
    
     private void exportarExcel() throws IOException {
        Date date = new Date();
        String fileName = "Curso" + date.getTime();
        File file = new File("C:\\COLEGIO\\Reportes", fileName + ".xls");
        TableModel model = jTable1.getModel();
        try (BufferedWriter excel = new BufferedWriter(new OutputStreamWriter(new FileOutputStream(file, true), "UTF-8"))) {
            for (int i = 0; i < model.getColumnCount(); i++) {
                excel.write(model.getColumnName(i) + "\t");
            }

            excel.write("\n");

            for (int i = 0; i < model.getRowCount(); i++) {
                for (int j = 0; j < model.getColumnCount(); j++) {
                    excel.write(model.getValueAt(i, j).toString() + "\t");
                }
                excel.write("\n");
            }
        }
        
        String message = "Reporte generado en la ubación C:\\COLEGIO\\Reportes con el nombre: " + fileName;
        JOptionPane.showMessageDialog(null, message);
    }

