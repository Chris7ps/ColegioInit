    private void llenarComboCurso() {
        DefaultComboBoxModel cbbModel = new DefaultComboBoxModel();
        cmbCurso.setModel(cbbModel);

        List<Curso> cursos = Cruds.CrudCurso.universo();
        cbbModel.addElement("");
        for (Curso curso : cursos) {
            cbbModel.addElement(curso);
        }
    }

    private void exportarExcel() throws IOException {
        Date date = new Date();
        String fileName = "AlumnosAsignados" + date.getTime();
        File file = new File("C:\\COLEGIO\\Reportes", fileName + ".xls");
        TableModel model = jTable1.getModel();
        try (BufferedWriter excel = new BufferedWriter(new OutputStreamWriter(new FileOutputStream(file, true), "UTF-8"))) {

            Curso curso = (Curso) cmbCurso.getSelectedItem();
            excel.write("Alumnos asignados a curso: " + curso.getNombreCurso());
            excel.write("\n");
            excel.write("\n");
            for (int i = 0; i < model.getColumnCount(); i++) {
                excel.write(model.getColumnName(i) + "\t");
            }

            excel.write("\n");

            for (int i = 0; i < model.getRowCount(); i++) {
                for (int j = 0; j < model.getColumnCount(); j++) {
                    excel.write(model.getValueAt(i, j).toString() + "\t");
                }
                excel.write("\n");
            }
        }

        String message = "Reporte generado en la ubaciÃ³n C:\\COLEGIO\\Reportes con el nombre: " + fileName;
        JOptionPane.showMessageDialog(null, message);
    }
