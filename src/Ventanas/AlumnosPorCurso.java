    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if ("".equals(cmbCurso.getSelectedItem())) {
            JOptionPane.showMessageDialog(null, "Seleccione un curso", "Failure", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                DefaultTableModel tableModel = new DefaultTableModel() {
                    public boolean isCellEditable(int row, int column) {
                        return false;
                    }
                };

                String[] columnas = {"Nombres", "Apellidos", "CUI", "Fecha de nacimiento", "Grado academico", "Genero", "Hora inicio", "Hora final"};
                tableModel.setColumnIdentifiers(columnas);
                jTable1.setModel(tableModel);
                Object[] fila = new Object[tableModel.getColumnCount()];
                Curso curso = (Curso) cmbCurso.getSelectedItem();
                List<Asignacion> asignaciones = Cruds.CrudAsignacion.alumnosAsignadosPorCurso(curso.getId());

                for (Asignacion asignacion : asignaciones) {
                    fila[0] = asignacion.getAlumno().getNombres();
                    fila[1] = asignacion.getAlumno().getApellidos();
                    fila[2] = asignacion.getAlumno().getCui();
                    fila[3] = new SimpleDateFormat("dd/MM/yyyy").format(asignacion.getAlumno().getFechaDeNacimiento());
                    fila[4] = asignacion.getAlumno().getGradoAlumno();
                    fila[5] = asignacion.getAlumno().getGenero();
                    fila[6] = asignacion.getHoraInicio();
                    fila[7] = asignacion.getHoraFinal();

                    tableModel.addRow(fila);
                }

            } catch (NullPointerException e) {
            }

            if (jTable1.getModel().getRowCount() > 0) {
                btnExportar.setEnabled(true);
            } else {
                btnExportar.setEnabled(false);
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

